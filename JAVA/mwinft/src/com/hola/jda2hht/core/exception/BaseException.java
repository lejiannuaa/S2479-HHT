package com.hola.jda2hht.core.exception;

/**
 * 异常基类。
 * 
 * @author 唐植超(上海软通)
 * @date 2012-12-27
 */
public class BaseException extends Exception {

	private static final long serialVersionUID = -1279773971168237118L;

	public BaseException(String message) {
		super(message);
	}

	public BaseException(Throwable cause) {
		super(cause);
	}

	public BaseException(String message, Throwable cause) {
		super(message, cause);
	}
}
