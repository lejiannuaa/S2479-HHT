public class ExtCountDetail {
	
	private int sel_no;
	private String sku;
	private String sku_dsc;
	private int sku_first_qty;
	private int sku_ext_qty;
	public int getSel_no() {
		return sel_no;
	}
	public void setSel_no(int sel_no) {
		this.sel_no = sel_no;
	}
	public String getSku() {
		return sku;
	}
	public void setSku(String sku) {
		this.sku = sku;
	}
	public String getSku_dsc() {
		return sku_dsc;
	}
	public void setSku_dsc(String sku_dsc) {
		this.sku_dsc = sku_dsc;
	}
	public int getSku_first_qty() {
		return sku_first_qty;
	}
	public void setSku_first_qty(int sku_first_qty) {
		this.sku_first_qty = sku_first_qty;
	}
	public int getSku_ext_qty() {
		return sku_ext_qty;
	}
	public void setSku_ext_qty(int sku_ext_qty) {
		this.sku_ext_qty = sku_ext_qty;
	}
	
	
}
